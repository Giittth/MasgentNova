# ---------- exit_and_cleanup 测试 ----------
def test_exit_and_cleanup(tmp_path, monkeypatch):
    """测试 exit_and_cleanup 函数（非空目录）"""
    monkeypatch.setenv("MASGENT_SESSION_RUNS_DIR", str(tmp_path))
    (tmp_path / "file.txt").write_text("content")

    with pytest.raises(SystemExit) as exc_info:
        utils.exit_and_cleanup()
    assert exc_info.type == SystemExit
    assert exc_info.value.code == 0


def test_exit_and_cleanup_empty_dir(tmp_path, monkeypatch):
    """测试 exit_and_cleanup 函数（空目录被删除）"""
    monkeypatch.setenv("MASGENT_SESSION_RUNS_DIR", str(tmp_path))

    with pytest.raises(SystemExit) as exc_info:
        utils.exit_and_cleanup()
    assert exc_info.type == SystemExit
    assert not tmp_path.exists()


