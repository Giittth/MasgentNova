# ---------- fit_eos 测试 ----------
def test_fit_eos():
    """测试 EOS 拟合函数"""
    # 生成随机体积和能量数据（不依赖已移除的 eos_func）
    volumes = np.linspace(90, 170, 10)
    energies = np.random.randn(10) * 0.1  # 模拟噪声

    volumes_fit, energies_fit = utils.fit_eos(volumes, energies)

    assert len(volumes_fit) == 100
    assert len(energies_fit) == 100
    assert volumes_fit[0] >= min(volumes) * 0.99
    assert volumes_fit[-1] <= max(volumes) * 1.01


